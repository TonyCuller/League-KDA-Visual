package Summoners;

/**
 * Stats Class for GSON Mapping
 */
public class Stats {
    public int championsKilled;
    public int assists;
    public int numDeaths;

    public int getChampionsKilled() {
        return championsKilled;
    }

    public int getAssists() {
        return assists;
    }

    public int getNumDeaths() {
        return numDeaths;
    }
}
